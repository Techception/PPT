#Hello world
message = "hello world"
print(message)

#Title case
print(message.title())

#Upper/Lower case 
name = "ada lovelace"
print(name.upper())
print(name.lower())

#f-String
first_name = "ada"
last_name = "lovelace"
full_name = f"{first_name} {last_name}"
print(full_name)

#f-string + Title case 
print(f"{full_name.title()}")

#WhiteSpace  
print("python")
print("\tpython",end="\n\n")

#new line
print("languages:\n\tPython\n\tC\n\tJava")

